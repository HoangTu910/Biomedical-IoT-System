/* USER CODE BEGIN 4 */
uint16_t CalculateCRC(uint8_t *data, uint16_t length)
{
    uint16_t crc = 0xFFFF; // Khởi tạo CRC với giá trị 0xFFFF
    for (uint16_t i = 0; i < length; i++)
    {
        crc ^= data[i]; // XOR dữ liệu với CRC
        for (uint8_t j = 0; j < 8; j++)
        {
            if (crc & 0x0001)
                crc = (crc >> 1) ^ 0x8408; // Sử dụng đa thức CRC-16-CCITT
            else
                crc >>= 1;
        }
    }
    return crc;
}
